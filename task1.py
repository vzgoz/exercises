def num_translate(num):
    if num == 'one':
        print('один')
    elif num == 'two':
        print('два')
    elif num == 'three':
        print('три')
    elif num == 'four':
        print('четыре')
    elif num == 'five':
        print('пять')
    elif num == 'six':
        print('шесть')
    elif num == 'seven':
        print('семь')
    elif num == 'eight':
        print('восемь')
    elif num == 'nine':
        print('девять')
    elif num == 'ten':
        print('десять')
    else:
        None

